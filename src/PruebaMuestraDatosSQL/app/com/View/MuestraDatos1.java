    // Esto previene colorear palabras que est√°n dentro de cadenas

    private boolean isInsideStringOrChar(String text, int position, Pattern stringPattern, Pattern charPattern) {

        // Verificar si la posici√≥n est√° dentro de una cadena "..."
        Matcher stringMatcher = stringPattern.matcher(text);
        while (stringMatcher.find()) {
            // Si la posici√≥n est√° entre el inicio y fin de una cadena
            if (position >= stringMatcher.start() && position < stringMatcher.end()) {
                return true;  // S√≠ est√° dentro de una cadena
            }
        }

        // Verificar si la posici√≥n est√° dentro de un car√°cter '...'
        Matcher charMatcher = charPattern.matcher(text);
        while (charMatcher.find()) {
            // Si la posici√≥n est√° entre el inicio y fin de un car√°cter
            if (position >= charMatcher.start() && position < charMatcher.end()) {
                return true;  // S√≠ est√° dentro de un car√°cter
            }
        }

        return false;  // No est√° dentro de ninguna cadena o car√°cter
    }

